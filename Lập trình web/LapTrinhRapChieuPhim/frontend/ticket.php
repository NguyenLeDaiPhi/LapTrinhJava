<script>
	$(document).ready(function () {
	  $('#bookingForm').on('submit', function (e) {
		e.preventDefault();
		let date = $('#selectDate').val();
		let time = $('#selectTime').val();
		
		if (!date || !time) {
		  alert('Vui lòng chọn ngày và giờ.');
		  return;
		}
  
		$('#bookingModal').modal('hide');
		setTimeout(function() {
		  $('#seatSelectionModal').modal('show');
		}, 500);
	  });
  
	  let seatContainer = $('.seat-map');
	  let rows = ['A', 'B', 'C', 'D', 'E'];
	  let cols = 10;
  
	  rows.forEach(row => {
		for (let i = 1; i <= cols; i++) {
		  let seat = $('<div class="seat available">' + row + i + '</div>');
		  seat.on('click', function () {
			if ($(this).hasClass('available')) {
			  $(this).toggleClass('selected');
			}
		  });
		  seatContainer.append(seat);
		}
	  });
  
	  // Khi modal ghế ngồi mở, xóa hết ghế đã chọn trước đó
	  $('#confirmSeats').on('click', function() {
		let selectedSeats = $('.seat.selected').map(function () {
			return $(this).text();
		}).get().join(',');

		if (selectedSeats.length === 0) {
			alert('Vui lòng chọn ít nhất một ghế.');
			return;
		}

		let movie_id = $('body').data('movie-id'); // Lấy movie_id từ data attribute
		let date = $('#selectDate').val();
		let time = $('#selectTime').val();

		if (!date || !time) {
			alert("Vui lòng chọn ngày và giờ chiếu.");
			return;
		}

		$.ajax({
			url: '../backend/process_ticket_booking.php',
			method: 'POST',
			data: {
			movie_id: movie_id,
			seats: selectedSeats,
			date: date,
			time: time
			},
			success: function (response) {
			let res = JSON.parse(response);
			if (res.status === "success") {
				alert("✅ " + res.message);
				window.location.href = "show_ticket.php";
			} else {
				alert("❌ " + res.message);
			}
			}
		});

		$('#seatSelectionModal').modal('hide');
		});
	});
  </script>
	

